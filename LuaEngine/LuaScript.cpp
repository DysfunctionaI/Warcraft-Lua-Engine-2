{
	CStringA lowerChat = chat;
	lowerChat.MakeLower();

	if (!m_chatevents[lowerChat.GetString()].empty())
	{
		LuaRef Ref = getGlobal(m_LuaState, m_chatevents[lowerChat.GetString()].c_str());
		try
		{
			Ref();
			return true;
		}
		catch (LuaException const &ex) { MessageBoxA(NULL, ex.what(), "Lua Script Error", MB_OK | MB_ICONINFORMATION); return false; }
	}
	else
		return false;
}

