bool CLuaScript::RegisterGameEvent(int32 nEvent, LPCSTR funcName)
{
	if (nEvent < EVENT_GAME_FINISHED || nEvent > EVENT_ITEM_CREATE)
		return false;

	m_events[nEvent] = std::string(funcName);

	return true;
}

bool CLuaScript::RegisterNativeHook(LPCSTR native, LPCSTR funcName_pre, LPCSTR funcName_post)
{
	if (native == nullptr)
		return false;

	if (strcmp(funcName_pre, "") !=0)
		m_nativehooks_pre[native] = std::string(funcName_pre);

	if (strcmp(funcName_post, "") != 0)
		m_nativehooks_post[native] = std::string(funcName_post);

	return true;
}

bool CLuaScript::RegisterChatEvent(LPCSTR chat, LPCSTR funcName)
{
	CStringA lowerChat = chat;
	lowerChat.MakeLower();

	size_t origLen = lowerChat.GetLength();

	for (auto &it : m_chateventslist)
	{
		if ((_strnicmp(lowerChat.GetString(), it.c_str(), it.length()) == 0) && (it.length() == origLen))
			return false;
	}

	m_chateventslist.push_back(std::string(lowerChat.GetString()));
	m_chatevents.insert(TStrStrPair(lowerChat.GetString(), funcName));

	return true;
}

void CLuaScript::HandleGameStateChange(int32 prevState, int32 newState)
{
	switch (newState)
	{
	case GS_NOTHING:
		if (prevState == GS_INGAME || prevState == GS_PAUSED)
			OnGameFinished();
		break;
	case GS_LOBBY:
		OnEnterLobby();
		break;
	case GS_LOADING:
		OnEnterLoading();
		break;
	case GS_INGAME:
		if (prevState == GS_LOADING)
			OnGameStarted();
		else if (prevState == GS_PAUSED)
			OnGameResumed();
		break;
	case GS_PAUSED:
		OnGamePaused();
	}
}

