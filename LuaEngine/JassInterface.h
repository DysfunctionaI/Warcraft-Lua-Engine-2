	static void OnNativeCalled_Post_NoParam(std::string name, TYPE _1)
	{
		for (auto script : ((CGameInterface*)lpParent)->m_Scripts){
			if (script != nullptr){
				((CLuaScript*)script)->OnNativeCalled_Post(name);
			}
		}
	}

	template <class TYPE>
	static TYPE OnNativeCalled_Post(std::string name, TYPE p1 = 0)
	{
		TYPE finalRet = p1;

		for (auto script : ((CGameInterface*)lpParent)->m_Scripts){
			if (script != nullptr){
				TYPE ret = ((CLuaScript*)script)->OnNativeCalled_Post(name, p1);
				if (ret != p1)
					finalRet = ret;
			}
		}

		return finalRet;
	}

